import axios from "axios";
import React, { useEffect, useState } from "react"
import { useParams } from "react-router-dom";
import Carousel, { slidesToShowPlugin } from '@brainhubeu/react-carousel';
import '@brainhubeu/react-carousel/lib/style.css';
import '../../../routesLogos-modules.css'
import LogosDestaqueInfomacoes from "./LogosDestaque";

const DestaqueLogos = () => {

  const [logos, setLogos] = useState('')
    


  useEffect(()=>{ 
    const fetchUsers = async () => { 
      try {
  
     await fetch(`${process.env.REACT_APP_BASE_URL_GALERY}/logomarcascarrossel`, {
        method:'GET',
        headers:{
          'Content-Type': 'aplication/json',
      }})
    .then(resp => resp.json())
     .then(data => {
      setLogos(data)
      

     })
     .catch((err) => console.log(err))

    
  } catch (error) {
        console.log({ error:'Erro ao buscar usu√°rios'})
        console.log(error)
  }
}
fetchUsers();

},[])





  return (
    <div className="destaque-Logos">
       <Carousel className="carrossel"
              plugins={[
                'centered',
                'infinite',
                'arrows',
                {
                  resolve: slidesToShowPlugin,
                  options: {
                    numberOfSlides: 1,
                  },
                },
              ]}
              breakpoints={{
                600: {
                  plugins: [
                    'centered',
                    'infinite',
                    'arrows',
                   {
                     resolve: slidesToShowPlugin,
                     options: {
                      numberOfSlides: 1
                     }
                   },
                 ],
                },
                900: {
                  plugins: [
                    'centered',
                    'infinite',
                    'arrows',
                   {
                     resolve: slidesToShowPlugin,
                     options: {
                      numberOfSlides: 2
                     }
                   },
                 ]
                 
                }
              }}
            >
     
     {logos.length > 0 &&
       logos.map((Logos) => (
         <LogosDestaqueInfomacoes
         titulo={Logos.titulo}
         Id={Logos._id}
         descricaoMin={Logos.descricaoMin}
         img={Logos.img}
         data={Logos.data}
         genero={Logos.genero}
         imgCapa={Logos.imgCapa}
         />
       )     

         )}
    
</Carousel>
    </div>
  )
}

export default DestaqueLogos